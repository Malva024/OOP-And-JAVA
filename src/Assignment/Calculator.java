package Assignment;

public class Calculator {

    public static float num1;
    public static float num2;

    public static float sum()
    {return num1 + num2;}

    public static float sub()
    {return num1 - num2;}

    public static float multi()
    {return num1 * num2;}

    public static float divid()
    {return num1 / num2;}

    public static float remainder()
    {
        return num1%num2;
    }

}
